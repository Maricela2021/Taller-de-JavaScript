// Ejemplo de valor para la nota, escenario 1: Aprobado
let nota = 4; 

if (nota >= 3) {
    console.log("Nota:", nota, "Aprobado");
} else {
    console.log("Nota:", nota, "Reprobado");
}

// solo cambiamos el valor, escenario 2: Reprobado
nota = 2; 

if (nota >= 3) {
    console.log("Nota:", nota, "Aprobado");
} else {
    console.log("Nota:", nota, "Reprobado");
}